package review;

public class Basictype {

	public static void main(String[] args) {
		// int, float, char, boolean
		int count = 20;
		double ratio = 0.25;
		char floor = '7';
		boolean hot = true;
		
		System.out.println("총 인원은 " + count + "원");
		System.out.println("총 비율은 " + ratio);
		System.out.println("층수는 " + floor);
		System.out.println("덥나요?" + hot);
	}

}
